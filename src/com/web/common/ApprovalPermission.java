package com.web.common;

import java.util.List;

import com.map.Sys_approve;
import com.map.code;

/**
 * @类功能说明：审批权限
 * @类修改者：
 * @修改日期：
 * @修改说明：
 * @作	    者：李存永
 * @创建时间：2013-12-5 下午3:10:00
 */
public class ApprovalPermission {

	private code code;						//代码表:存储 审批状态
	private List<Sys_approve> approList;	//审批权限集合
	public code getCode() {
		return code;
	}
	public void setCode(code code) {
		this.code = code;
	}
	public List<Sys_approve> getApproList() {
		return approList;
	}
	public void setApproList(List<Sys_approve> approList) {
		this.approList = approList;
	}
	@Override
	public String toString() {
		return "ApprovalPermission [code=" + code + ", approList=" + approList
				+ "]";
	}
	
}
